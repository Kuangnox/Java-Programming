public class AreaAndPerimeterOfARectangle {
    public static void main(String[] args) {
        double perimeter;//周长
        double area;//面积
        double width=4.5,height=7.9;
        perimeter=2*width*height;
        area = width * height;
        System.out.println(perimeter);
        System.out.println(area);
    }
}
